package com.NewCenturyHotels.NewCentury.bean;

public class HomeGrid {
    private String name;
    private Object img;
    private String redirectSrc;
    private String orderById;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Object getImg() {
        return img;
    }

    public void setImg(Object img) {
        this.img = img;
    }

    public String getRedirectSrc() {
        return redirectSrc;
    }

    public void setRedirectSrc(String redirectSrc) {
        this.redirectSrc = redirectSrc;
    }

    public String getOrderById() {
        return orderById;
    }

    public void setOrderById(String orderById) {
        this.orderById = orderById;
    }
}
